def imp2metric(s):
  #6-6
  feet, inches = s.split("-")
  return int(feet)*30.2 + int(inches)*2.54

df.dropna(axis=0, how="all", inplace=True)
df.tail()

df["Height_cm"] = df["Height"].apply(imp2metric)

